# Sum of the min & max of (a, b, c)
def hilo(a, b, c):
    if c < b: b, c = c, b
    if b < a: a, b = b, a
    if c < b: b, c = c, b
    return a + c


def complement(color):
    k = hilo(color[0], color[1], color[2])
    return tuple(k - u for u in color)
